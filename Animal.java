package zooologico;

public class Animal {

	int No_animal;
	String Nombre;
	String Nacimiento;
	String Especie;
	
	public int getNo_animal() {
		return No_animal;
	}
	public void setNo_animal(int no_animal) {
		No_animal = no_animal;
	}
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public String getNacimiento() {
		return Nacimiento;
	}
	public void setNacimiento(String nacimiento) {
		Nacimiento = nacimiento;
	}
	public String getEspecie() {
		return Especie;
	}
	public void setEspecie(String especie) {
		Especie = especie;
	}
	
	
}
